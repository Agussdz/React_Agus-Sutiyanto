// Function Declaration
function greetUser(name) {
  return `Halooo, ${name} Function Declaration`;
}

// Arrow Function
const greetUserArrow = (name) => {
  return `Disini, ${name} Function`;
};
console.log(greetUser("ini")); // Output: Halooo, ini Function Declaration
console.log(greetUserArrow("Arrow")); // Output: Disini, Arrow Function
